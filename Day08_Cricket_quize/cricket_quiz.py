# ── STAGE 2: ASK QUESTION FUNCTION ──────────────────────────

def ask_question(q_num, q):
    """Display one question, get and validate the answer, return True/False."""
    print(f"\nQ{q_num}. {q['question']}")
    print(f"   A) {q['options'][0]}")
    print(f"   B) {q['options'][1]}")
    print(f"   C) {q['options'][2]}")
    print(f"   D) {q['options'][3]}")

    while True:
        answer = input("   Your answer (A/B/C/D): ").strip().upper()
        if answer in ["A", "B", "C", "D"]:
            break
        print("   ⚠️  Invalid input. Please enter A, B, C or D.")

    if answer == q["answer"]:
        print("   ✅ Correct!")
        print(f"   💡 {q['explanation']}")
        return True
    else:
        print(f"   ❌ Wrong! Correct answer is {q['answer']}.")
        print(f"   💡 {q['explanation']}")
        return False


# ── STAGE 3: GRADE FUNCTION ─────────────────────────────────

def get_grade(score, total):
    """Return grade and feedback based on score."""
    percent = (score / total) * 100

    if percent == 100:
        return "Complete Cricket Encyclopedia", "Flawless! Stars and unsung heroes alike — you know it all."
    elif percent >= 70:
        return "Serious Cricket Buff", "Great job! You clearly follow both the headlines and the deep cuts."
    elif percent >= 40:
        return "Casual Follower", "Decent effort! Some of these stats — famous or obscure — slipped past you."
    else:
        return "Needs More Homework", "Tough quiz! Time to dig into Statsguru for both the legends and the underrated greats."


